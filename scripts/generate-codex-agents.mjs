#!/usr/bin/env node
/**
 * Generate OpenAI Codex custom agent TOML files from CodeCortexLoop agents/*.md.
 * Usage: node generate-codex-agents.mjs <source-agents-dir> <dest-agents-dir>
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const agentsSrc = process.argv[2];
const agentsDest = process.argv[3];

if (!agentsSrc || !agentsDest) {
  console.error('[cortexloop] Usage: node generate-codex-agents.mjs <source-agents-dir> <dest-agents-dir>');
  process.exit(1);
}

function parseAgentMarkdown(text) {
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) return null;
  const frontmatter = {};
  for (const line of match[1].split(/\r?\n/)) {
    const idx = line.indexOf(':');
    if (idx === -1) continue;
    const key = line.slice(0, idx).trim();
    const value = line.slice(idx + 1).trim();
    if (key) frontmatter[key] = value;
  }
  return { frontmatter, body: match[2].trim() };
}

function tomlBasicString(value) {
  return `"${String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

function tomlMultiline(value) {
  const normalized = String(value).replace(/\r\n/g, '\n');
  if (normalized.includes('"""')) {
    throw new Error('Agent body contains \"\"\" — cannot encode as TOML multiline string');
  }
  return `"""\n${normalized}\n"""`;
}

mkdirSync(agentsDest, { recursive: true });

let written = 0;
for (const file of readdirSync(agentsSrc).filter((f) => f.endsWith('.md'))) {
  const parsed = parseAgentMarkdown(readFileSync(join(agentsSrc, file), 'utf8'));
  if (!parsed) {
    console.warn(`[cortexloop] skip ${file}: no frontmatter`);
    continue;
  }
  const name = parsed.frontmatter.name || file.replace(/\.md$/, '');
  const description = parsed.frontmatter.description || `CodeCortexLoop expert ${name}`;
  const instructions = `${parsed.body}\n\n## Codex spawn obligations\n\nWhen spawned as a CodeCortexLoop subagent, write required deliverables to disk before returning. Read prior handoff JSON paths passed in the spawn prompt. Do not spawn nested subagents.`;

  const toml = `# CodeCortexLoop expert — generated by install-codex (re-run install to refresh)
name = ${tomlBasicString(name)}
description = ${tomlBasicString(description)}
developer_instructions = ${tomlMultiline(instructions)}
sandbox_mode = "workspace-write"
model_reasoning_effort = "high"
`;

  const outPath = join(agentsDest, `${name}.toml`);
  writeFileSync(outPath, toml, 'utf8');
  written += 1;
}

console.log(`[cortexloop] Codex agents generated: ${written} file(s) in ${agentsDest}`);
